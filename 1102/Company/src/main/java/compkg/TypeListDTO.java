package compkg;

public class TypeListDTO {

	private String code;
	private String typeName;
	
	//Setter
	public void setCode(String code)
	{
		this.code = code;
	}
	
	public void setTypeName(String name)
	{
		this.typeName = name;
	}
	
	
	//Getter
	public String getCode()
	{
		return this.code;
	}
	
	public String getTypeName()
	{
		return this.typeName;
	}
}
